public class OperandosMatematicos {
 public static void main(String[] args) {
 int valor = 5, num = 13;

 System.out.println( valor * num );
 System.out.println( valor + num );
 System.out.println( num / valor );
 System.out.println( num % valor );
 System.out.println( num - valor );
 }
}
